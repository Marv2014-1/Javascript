// Conditionals: Ternary Operators

// Syntax
let expression = true;
let result = expression ? 'this is true' : 'this is false';

console.log(result);

// chained ternary operators
let day = "wednesday";
let isWeekend = day === "saturday" || day === "sunday" ? "weekend" : "weekday";
console.log(isWeekend);